#include <iostream>
#include <vector>

using namespace std;

//? ฟังก์ชัน Binary Search
//* คืนค่า index ที่เจอ, ถ้าไม่เจอคืนค่า -1
int binarySearch(const vector<int>& arr, int target) {
    int low = 0;
    int high = arr.size() - 1;
    while (low <= high) { //! ต้องใช้ <= เพราะถ้า low == high ยังมีโอกาสเจออยู่
        //* หาตำแหน่งตรงกลาง (ใช้สูตรนี้เพื่อกันค่าล้น หรือ overflow เมื่อข้อมูลเยอะมากๆ)
        int mid = low + (high - low) / 2; //* อาจใช้ (low + high) / 2 ก็ได้ถ้าข้อมูลไม่เยอะ
        //* กรณี 1: เจอแล้ว! (อยู่ตรงกลางพอดี)* 
        if (arr[mid] == target)
            return mid;
        //* กรณี 2: ค่าที่หา "มากกว่า" ตรงกลาง -> ต้องไปหาฝั่งขวา
        if (arr[mid] < target)
            low = mid + 1;
        //* กรณี 3: ค่าที่หา "น้อยกว่า" ตรงกลาง -> ต้องไปหาฝั่งซ้าย
        else
            high = mid - 1;
    }
    return -1; //* หาจนทั่วแล้ว (low แซง high) ก็ยังไม่เจอ
}

int main() {
    //! *สำคัญมาก* ข้อมูลต้องเรียงลำดับแล้ว
    vector<int> data = {2, 5, 8, 12, 16, 23, 38, 56, 72, 91};
    int target = 23;
    int result = binarySearch(data, target);
    if(result != -1)
        cout << "Found " << target << " at index: " << result << endl;
    else
        cout << "Not found" << endl;
    return 0;
}

