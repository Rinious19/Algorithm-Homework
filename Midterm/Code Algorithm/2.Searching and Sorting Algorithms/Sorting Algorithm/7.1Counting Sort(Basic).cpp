#include <iostream>
#include <vector>
#include <algorithm> //* สำหรับหา max_element
using namespace std;

void basicCountingSort(vector<int>& arr){
    if(arr.empty()) return;
    //@ 1. หาค่าสูงสุด (Max) เพื่อจองขนาด Array ให้พอดี
    int maxVal = *max_element(arr.begin(), arr.end());
    //@ 2. สร้าง Array สำหรับนับ (Count Array)
    //* ขนาดต้องเป็น maxVal + 1 (เพราะ index เริ่มที่ 0 ถึง maxVal)
    vector<int> count(maxVal + 1, 0);
    //* 3. เริ่มนับความถี่ (Count Frequencies) ของแต่ละค่าใน arr
    for(int num : arr)
        count[num]++;
    //@ 4. เขียนข้อมูลกลับลงไปใน arr เดิม (Overwrite)
    int index = 0; //* ตัวชี้ตำแหน่งที่จะเขียนใน arr
    for(int i = 0; i <= maxVal; i++){
        //* วนลูปตามจำนวนที่นับได้ใน count[i]
        while(count[i] > 0){
            arr[index] = i; //* เขียนเลข i ลงไป
            index++;        //* ขยับไปช่องถัดไป
            count[i]--;     //* ลดจำนวนที่ต้องเขียนลง
        }
    }
}

int main(){
    vector<int> data = {2, 5, 3, 0, 2, 3, 0, 3};
    cout<<"Original: ";
    for(int x : data) 
        cout<<x<<" ";
    cout<<endl;
    basicCountingSort(data);
    cout<<"Sorted:   ";
    for(int x : data) 
        cout<<x<<" ";
    cout<<endl;
    return 0;
}

