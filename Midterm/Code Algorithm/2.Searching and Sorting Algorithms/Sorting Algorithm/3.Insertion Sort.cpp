#include <bits/stdc++.h>
using namespace std;

void insertionSort(vector<int>& arr){
    int n = arr.size();    
    //* เริ่มที่ i = 1 (ตัวที่สอง) เพราะถือว่าตัวแรก (i=0) เรียงแล้ว
    for(int i = 1; i < n; i++){
        int key = arr[i]; //* หยิบไพ่มาถือไว้
        int j = i - 1;    //* เตรียมเทียบกับตัวก่อนหน้า
        //@ ลูปนี้ทำหน้าที่: "ขยับตัวที่มากกว่า key ไปทางขวา"
        //! เงื่อนไข: j ต้องไม่หลุดขอบซ้าย (>=0) และ ตัวที่เช็ค (arr[j]) ต้องมากกว่า key เพื่อที่จะสลับไปทางขวา
        while(j >= 0 && arr[j] > key){
            arr[j + 1] = arr[j]; //* ขยับตัวนี้ไปขวา 1 ช่อง
            j = j - 1;           //* ถอยไปเช็คตัวถัดไปทางซ้าย
        }
        //* เมื่อหลุดลูป แปลว่าเจอตำแหน่งที่ถูกต้องแล้ว (หรือสุดขอบ)
        arr[j + 1] = key; //* วาง key ลงไป ที่ตำแหน่ง j + 1
    }
}

int main(){
    vector<int> data = {12, 11, 13, 5, 6};
    cout<<"Original: ";
    for(int x : data) 
        cout<<x<<" ";
    cout<<endl;
    insertionSort(data); //* Call Function 
    cout<<"Sorted:   ";
    for(int x : data) 
        cout<<x<<" ";
    cout<<endl;
    return 0;
}

