#include <bits/stdc++.h>
using namespace std;

void shellSort(vector<int>& arr) {
    int n = arr.size();
    //@ Loop 1: เริ่มต้น Gap ที่ครึ่งหนึ่ง (n/2) และลดลงทีละครึ่ง
    for (int gap = n / 2; gap > 0; gap /= 2) {
        //@ Loop 2: ทำ Insertion Sort แบบก้าวกระโดดตามค่า Gap
        //* (ไล่ตั้งแต่ตัวที่ gap ไปจนถึงตัวสุดท้าย)
        for (int i = gap; i < n; i++) {
            int temp = arr[i]; //* ตัวที่จะเอาไปแทรก
            int j = i; //* ตำแหน่งปัจจุบันของตัวที่กำลังพิจารณา
            //@ Loop 3: เช็คและขยับข้อมูล (Shift)
            //* คล้าย Insertion Sort แต่แทนที่จะเช็คตัวติดกัน (j-1)
            //* เราเช็คตัวที่ห่างไปเท่ากับ gap (j - gap)
            while (j >= gap && arr[j - gap] > temp) {
                arr[j] = arr[j - gap];
                j -= gap;
            }
            arr[j] = temp; //* วางลงในตำแหน่งที่ถูกต้อง
        }
    }
}

int main() {
    vector<int> data = {12, 34, 54, 2, 3};
    cout << "Original: ";
    for (int x : data) 
        cout << x << " ";
    cout << endl;
    shellSort(data);
    cout << "Sorted:   ";
    for (int x : data) 
        cout << x << " ";
    cout << endl;
    return 0;
}

