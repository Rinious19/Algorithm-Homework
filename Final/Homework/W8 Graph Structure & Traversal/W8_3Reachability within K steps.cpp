#include <bits/stdc++.h>
using namespace std;

//? ฟังก์ชัน BFS สำหรับหาระยะทางและนับจำนวนโหนดที่ไปไม่ถึงภายในระยะ maxDist
int countUnreachableNodes(const vector<vector<int>>& graph, int startNode, int maxDist){
    //@ 1. สร้าง Array เก็บระยะทาง (เผื่อขนาด nVertex + 1 เพราะโหนดเริ่มนับที่ 1)
    //* กำหนดค่าเริ่มต้นเป็น -1 ทั้งหมด (-1 หมายถึง "ยังไปไม่ถึง")
    vector<int> distance(graph.size() , -1); 
    queue<int> q;
    //@ 2. ตั้งค่าเริ่มต้นให้กับจุด Start (startNode)
    distance[startNode] = 0; //* ระยะทางจากตัวเองไปหาตัวเองคือ 0
    q.push(startNode); // เอาจุดเริ่มต้นเข้าคิว
    //@ 3. เริ่มกระบวนการค้นหาแบบ BFS (แผ่ออกทีละชั้น)
    while(!q.empty()){
        int currentNode = q.front(); //* ดึงค่าของตัวที่อยู่หน้าสุดของคิวออกมาเพื่อประมวลผล
        q.pop();
        //* เช็กเพื่อนบ้านทุกตัวที่สามารถเดินไปได้ (กราฟมีทิศทาง)
        for (int i = 0; i < graph[currentNode].size(); i++){
            int neighbor = graph[currentNode][i];
            //* ถ้าเพื่อนบ้านตัวนี้ยังไม่เคยไปเยือน (distance เป็น -1)
            if (distance[neighbor] == -1){
                //* อัปเดตระยะทาง = ระยะทางของจุดปัจจุบัน + 1 ก้าว
                distance[neighbor] = distance[currentNode] + 1;
                //* เอาเพื่อนบ้านเข้าคิวเพื่อไปขยายผลต่อในรอบหน้า
                q.push(neighbor);
            }
        }
    }
    //@ 4. สรุปผล: นับจำนวนโหนดที่ไม่ผ่านเงื่อนไข
    int unreachableCount = 0;
    for (int i = 1; i < distance.size(); i++){
        //* เงื่อนไข: ถ้าไปไม่ถึงเลย (-1) หรือ ระยะทางเกินโควตาที่กำหนด (> maxDist)
        if (distance[i] == -1 || distance[i] > maxDist)
            unreachableCount++;
    }
    return unreachableCount; //* ส่งค่าที่นับได้กลับไป
}

int main(){
    int nVertex;
    cin>>nVertex;// รับจำนวนโหนด
    //* สร้าง Adjacency List (ขนาด nVertex + 1)
    vector<vector<int>> graph(nVertex + 1);
    int start, end;
    //@ รับค่าเส้นเชื่อมไปเรื่อยๆ ตามเงื่อนไขโจทย์
    while(cin>>start>>end){
        // หยุดรับเส้นเชื่อมเมื่อเจอ 0 0
        if (start == 0 && end == 0)
            break;
        //* กราฟมีทิศทาง (Directed Graph) ชี้จาก start ไป end ทางเดียว
        graph[start].push_back(end); 
    }
    int startNode, maxDist;
    //@ บรรทัดสุดท้าย รับจุดเริ่มต้น (startNode) และระยะทางสูงสุด (maxDist)
    cin>>startNode>>maxDist;
    //* เรียกใช้งานฟังก์ชันและพิมพ์ผลลัพธ์
    int result = countUnreachableNodes(graph, startNode, maxDist);
    cout<<result<<"\n";
    return 0;
}

