#include <bits/stdc++.h>
using namespace std;

//? ฟังก์ชันหาความยาวเส้นทางที่สั้นที่สุดด้วย BFS
int findShortestPath(const vector<vector<int>> graph, int startNode, int destNode){
    //* สร้าง array เก็บระยะทาง (distance) กำหนดค่าเริ่มต้นเป็น -1 (หมายถึงยังไม่เคยไป)
    vector<int> distance(graph.size(), -1);  //* graph.size() จะได้ขนาด nVertex + 1 เพราะโหนดเริ่มนับที่ 1
    queue<int> q;
    //@ 1. ตั้งค่าเริ่มต้นให้จุด Start
    distance[startNode] = 0; // ระยะทางจากตัวเองถึงตัวเองคือ 0
    q.push(startNode); // เอาจุดเริ่มต้นเข้าคิว
    //@ 2. เริ่มแผ่ขยาย (BFS)
    while(!q.empty()){
        int currentNode = q.front(); //* ดึงค่าของตัวที่อยู่หน้าสุดของคิวออกมาเพื่อประมวลผล
        q.pop();
        //* 💡 เช็กว่าถึงจุดหมายปลายทางหรือยัง?
        // ถ้าถึงแล้ว เราสามารถส่งคืน (return) ระยะทางแล้วจบการทำงานได้เลยทันที!
        if(currentNode == destNode){
            return distance[currentNode];
        }
        //* ค้นหาเพื่อนบ้านที่เชื่อมต่อกับจุดปัจจุบัน
        for(int i = 0; i < graph[currentNode].size(); i++){
            int neighbor = graph[currentNode][i];
            //* ถ้าเพื่อนบ้านจุดนั้นเรายังไม่เคยแวะไป (distance เป็น -1)
            if(distance[neighbor] == -1){
                //* บวกระยะทางเพิ่มไปอีก 1 ก้าว จากจุดที่ยืนอยู่
                distance[neighbor] = distance[currentNode] + 1;
                q.push(neighbor); //! จับเพื่อนบ้านเข้าคิวเพื่อตรวจสอบต่อในรอบหน้า
            }
        }
    }
    return -1; //* กรณีหาเส้นทางไม่เจอ (ไปไม่ถึง)
}

int main(){
    int nVertex; // รับจำนวนโหนดทั้งหมด
    cin>>nVertex;
    //* สร้าง Adjacency List ขนาด nVertex + 1 (โหนดเริ่มนับที่ 1)
    vector<vector<int>> graph(nVertex + 1);
    //@ รับข้อมูลเส้นเชื่อม (ตามจำนวนโหนด nVertex บรรทัด)
    for(int i = 0; i < nVertex; i++){
        int startNode, neighbor;
        cin>>startNode; // โหนดต้นทาง
        //* รับเพื่อนบ้านไปเรื่อยๆ จนกว่าจะเจอ 0
        while(cin>>neighbor && neighbor != 0){
            graph[startNode].push_back(neighbor);
        }
    }
    //@ บรรทัดสุดท้าย: รับจุดเริ่มต้น (startNode) และ จุดปลายทาง (destNode)
    int startNode, destNode;
    cin>>startNode>>destNode;
    //* เรียกใช้ฟังก์ชันและพิมพ์ผลลัพธ์
    int shortestPath = findShortestPath(graph, startNode, destNode);
    cout<<shortestPath<<endl;
    return 0;
}

