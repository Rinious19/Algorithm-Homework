#include <bits/stdc++.h>
using namespace std;

int numRows, numCols; // จำนวนแถว (numRows), คอลัมน์ (numRows)
vector<string> grid; //* ตารางเก็บสีของแต่ละช่อง (ใช้ string เพื่อเก็บข้อมูลทีละบรรทัด)
int filledPixelCount = 0; //* ตัวแปรเก็บจำนวนพิกเซลที่ถูกระบายสี

//* อาร์เรย์ช่วยทิศทาง: ขวา(0, 1), ซ้าย(0, -1), ล่าง(1, 0), บน(-1, 0) (คู่ currentRow, currentCol)
int rowDirection[] = {0, 0, 1, -1};
int colDirection[] = {1, -1, 0, 0};

//? ฟังก์ชัน floodFillDFS สำหรับลามสี
void floodFillDFS(int currentRow, int currentCol, char targetColor) {
    //@ 1. เช็กขอบเขต (Base Case): ห้ามออกนอกตาราง
    if(currentRow < 0 || currentRow >= numRows || currentCol < 0 || currentCol >= numCols)
        return;
    //@ 2. ถ้าสีช่องปัจจุบัน ไม่ใช่เป้าหมายที่ต้องการเติม หรือ เติมไปแล้ว ให้หยุด
    if(grid[currentRow][currentCol] != targetColor)
        return;
    //@ 3. ระบายสี! (เปลี่ยนอักษรเพื่อมาร์คว่ามาแล้ว) และเพิ่มตัวนับ
    grid[currentRow][currentCol] = '#'; //* เปลี่ยนเป็นสัญลักษณ์อะไรก็ได้ที่ไม่ใช่สีเดิม
    filledPixelCount++;
    //@ 4. กระจายตัวไป 4 ทิศทาง คือ ขวา, ซ้าย, ล่าง, บน จากตำแหน่งปัจจุบัน
    for(int i = 0; i < 4; i++){
        int nextRow = currentRow + rowDirection[i];
        int nextCol = currentCol + colDirection[i];
        floodFillDFS(nextRow, nextCol, targetColor);
    }
}

int main() {
    cin>>numRows>>numCols;
    //* สร้างตาราง
    grid.resize(numRows); //* resize คือ การกำหนดขนาดของ vector ให้มี numRows แถว
    for(int i = 0; i < numRows; i++)
        cin>>grid[i]; // รับ string ทีละบรรทัด
    //* รับตำแหน่งที่คลิก
    int clickRow, clickCol;
    cin>>clickRow>>clickCol;
    //* แปลงพิกัดให้เป็นแบบ Array (เริ่มที่ 0)
    int startRow = clickRow - 1;
    int startCol = clickCol - 1;
    //* เก็บสีเริ่มต้นที่เราคลิกโดนเอาไว้
    char targetColor = grid[startRow][startCol];
    // เรียกใช้ฟังก์ชันเติมสี
    floodFillDFS(startRow, startCol, targetColor);
    // แสดงผลจำนวนช่องที่ถูกเติม
    cout<<filledPixelCount<<endl;
    return 0;
}

