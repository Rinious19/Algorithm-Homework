#include <bits/stdc++.h>
using namespace std;

//? ฟังก์ชันหาโหนดที่ "เข้าถึงไม่ได้" จากจุดเริ่มต้น (startNode)
void findUnreachableNode(const vector<vector<int>>& graph, int startNode){
    //* สร้าง array เก็บสถานะการไปถึง (เริ่มต้นเป็น false ให้หมด)
    vector<bool> visited(graph.size(), false);  //* graph.size() จะได้ขนาด nVertex + 1 เพราะโหนดเริ่มนับที่ 1
    queue<int> q;
    //@ 1. เริ่มต้นที่จุด startNode
    q.push(startNode);
    visited[startNode] = true; //* ทำเครื่องหมายว่าตัวเองเข้าถึงตัวเองได้เสมอ
    //@ 2. ใช้ BFS แผ่ขยายไปตามเส้นทางที่กราฟชี้ไป
    while(!q.empty()){
        int currentNode = q.front(); //* ดึงค่าของตัวที่อยู่หน้าสุดของคิวออกมาเพื่อประมวลผล
        q.pop();
        //* เช็กเพื่อนบ้านทุกตัวที่ currentNode ชี้ไป
        for(int i = 0; i < graph[currentNode].size(); i++){
            int neighbor = graph[currentNode][i];
            //* ถ้าเพื่อนบ้านตัวนี้ยังไม่เคยไปถึง ให้จับเข้าคิวและทำเครื่องหมาย
            if(!visited[neighbor]){
                visited[neighbor] = true;
                q.push(neighbor);
            }
        }
    }
    //@ 3. สรุปผล: ตรวจสอบโหนดที่เข้าถึงไม่ได้
    bool allReached = true; //* ตัวแปรเช็กว่าไปถึงได้หมดทุกตัวไหม
    for(int i = 1; i < graph.size(); i++){
        if(!visited[i]){
            //* ถ้าโหนด i ไม่ถูกไปเยือน แปลว่าไปไม่ถึง ให้พิมพ์ออกมา
            cout<<i<<" "; 
            allReached = false;
        }
    }
    //* ถ้าไปถึงได้ทุกโหนด (ไม่มีโหนดไหนตกหล่น) ให้พิมพ์ 0
    if(allReached)
        cout<<0;
    cout<<endl;
}

int main(){
    int nVertex; //* รับจำนวนโหนดทั้งหมด
    cin>>nVertex;
    //* สร้างกราฟขนาด nVertex + 1 (เพราะโหนดเริ่มนับที่ 1)
    vector<vector<int>> graph(nVertex + 1);
    //@ รับค่าเส้นเชื่อม (Edge) จำนวน nVertex บรรทัด
    for(int i = 0; i < nVertex; i++){
        int startNode, neighbor;
        cin>>startNode; //* โหนดต้นทาง
        //* รับโหนดปลายทางไปเรื่อยๆ จนกว่าจะเจอเลข 0
        while(cin>>neighbor && neighbor != 0)
            graph[startNode].push_back(neighbor); //* กราฟมีทิศทาง ชี้ไปทางเดียว
    }
    //@ บรรทัดสุดท้าย: รับจำนวนชุดการทดสอบ round
    int round;
    cin>>round;
    //* รับโหนดเริ่มต้น v1, v2, ... v_nVertex และเรียกใช้ฟังก์ชันทีละตัว
    for(int i = 0; i < round; i++){
        int startNode;
        cin>>startNode;
        //* เรียกฟังก์ชันเพื่อหาโหนดที่ไปไม่ถึงจาก startNode นั้นๆ
        findUnreachableNode(graph, startNode);
    }
    return 0;
}

