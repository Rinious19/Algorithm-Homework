#include <bits/stdc++.h>
using namespace std;

void bubbleSort(vector<double>& arr){
    int n = arr.size() - 1;
    for(int i = 0; i < n - 1; i++){
        for(int j = 0; j < n - i - 1; j++){
            if(arr[j] > arr[j + 1])
                swap(arr[j], arr[j+1]);
        }
    }
}

int main(){
    int n;
    cin>>n; //* รับจำนวนขบวนรถไฟ
    vector<double> arr(n); //* เก็บเวลา Arrival
    vector<double> dep(n); //* เก็บเวลา Departure
    //* รับข้อมูลเวลาเข้าและออก
    for(int i = 0; i < n; i++)
        cin>>arr[i]>>dep[i];
    //@ 1. เรียงลำดับเวลาเข้า และ เวลาออก (แยกกัน)
    bubbleSort(arr);
    bubbleSort(dep);
    int currentPlatforms = 0; //* จำนวนชานชาลาที่กำลังใช้งานอยู่ ณ ขณะนั้น
    int maxPlatforms = 0; //* จำนวนชานชาลาที่ใช้งานพร้อมกันมากที่สุด (คำตอบ)
    int i = 0; //* Pointer สำหรับเวลาเข้า (arr)
    int j = 0; //* Pointer สำหรับเวลาออก (dep)
    //@ 2. ใช้ 2 Pointers ไล่เทียบเหตุการณ์ตามลำดับเวลา
    while(i < n && j < n){ //* ไล่จนครบทั้ง arrival & departure time
        //* ถ้ารถไฟขบวนต่อไป "เข้ามาถึง" ก่อนที่รถไฟขบวนก่อนหน้าจะ "ออกไป"
        //* แสดงว่าเกิดการทับซ้อน ต้องใช้ชานชาลาเพิ่ม
        if (arr[i] < dep[j]){
            currentPlatforms++;
            maxPlatforms = max(maxPlatforms, currentPlatforms); //* อัปเดตค่าสูงสุด
            i++; //* ขยับไปดูเหตุการณ์รถเข้าคันต่อไป
        } 
        //* ถ้ารถไฟขบวนก่อนหน้า "ออกไปแล้ว" หรือ "ออกไปพร้อมกับคันใหม่ที่เข้ามาพอดี"
        //* แสดงว่าเราได้ชานชาลาคืนมา
        else {
            currentPlatforms--;
            j++; //* ขยับไปดูเหตุการณ์รถออกคันต่อไป
        }
    }
    cout<<maxPlatforms<<endl; //* พิมพ์คำตอบ
    return 0;
}

