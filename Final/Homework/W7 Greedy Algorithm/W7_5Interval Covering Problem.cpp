#include <bits/stdc++.h>
using namespace std;

//? สร้าง Class สำหรับเก็บจุดเริ่มและจุดจบของเส้นตรง
class Line {
public:
    int start;
    int end;
};

//? เรียงจุดเริ่มต้นจากน้อยไปมาก ถ้าเริ่มพร้อมกันให้เอาเส้นยาวขึ้นก่อน
void sortLineAscending(vector<Line>& arr){
    int n = arr.size();
    for(int i = 0; i < n - 1; i++){
        for(int j = 0; j < n - i - 1; j++){
            //* start เรียงแบบ Ascending
            if(arr[j].start > arr[j + 1].start)
                swap(arr[j], arr[j + 1]);
            //* ถ้า start เท่ากัน เรียง end แบบ Descending
            else if(arr[j].start == arr[j + 1].start
                   && arr[j].end < arr[j + 1].end){
                   swap(arr[j], arr[j + 1]);
            }
                
        }
    }
}

int main(){
    int n;
    cin>>n;
    vector<Line> lines(n);
    for(int i = 0; i < n; i++){
        cin>>lines[i].start>>lines[i].end;
    }
    int targetStart, targetEnd;
    cin>>targetStart>>targetEnd;
    //@ 1. เรียงข้อมูลเส้นตรงทั้งหมดจากน้อยไปมากตามจุดเริ่มต้น (และถ้าเริ่มพร้อมกันให้เอาเส้นที่ยาวกว่าไว้ก่อน)
    sortLineAscending(lines);
    vector<Line> chosenLines; //* เก็บเส้นที่ถูกเลือกมาแล้ว
    int currentStart = targetStart; //* จุดที่เรายืนอยู่ปัจจุบัน
    int i = 0; //* ตัวชี้สำหรับวนผ่านเส้นตรงทั้งหมด เพื่อหาเส้นที่ต่อได้จาก currentStart
    //@ 2. เริ่มลูปหาเส้นตรง (Greedy) 
    while(currentStart < targetEnd){ //* ถ้ายังไม่ถึงจุดหมายปลายทาง
        int maxEnd = INT_MIN; //* ตัวแปรเก็บระยะที่ยื่นไปไกลที่สุดในรอบนี้
        int bestIndex = -1;  //* เก็บ index ของเส้นที่ถูกเลือก
        //* หาเส้นที่สามารถ "ต่อ" จาก currentStart ได้
        while(i < n && lines[i].start <= currentStart){
            if(lines[i].end > maxEnd){
                maxEnd = lines[i].end;
                bestIndex = i;
            }
            i++; //* ขยับไปดูเส้นถัดไปที่อาจต่อได้ในรอบนี้
        }
        //* ถ้าหาเส้นต่อไม่ได้เลย (เกิดช่องโหว่) แปลว่าทำไม่สำเร็จ
        if(bestIndex == -1){
            cout<<"0\n"; //* หรือแสดงผลตามที่อาจารย์ตกลงไว้กรณีหาไม่เจอ
            return 0;
        }
        chosenLines.push_back(lines[bestIndex]); //* เก็บเส้นที่เลือกลงในคำตอบ
        currentStart = maxEnd; //* อัปเดตจุดยืนให้ขยับไปที่ปลายทางของเส้นที่เพิ่งเลือก
    }
    //@ 3. ปริ้นท์คำตอบ
    cout<<chosenLines.size()<<endl;
    //* โจทย์สั่งให้เรียงคำตอบตามลำดับ xi, xj น้อยไปมากก่อนปริ้นท์
    sortLineAscending(chosenLines);
    for(const auto& line : chosenLines){
        cout<<line.start<<" "<<line.end<<endl;
    }
    return 0;
}

