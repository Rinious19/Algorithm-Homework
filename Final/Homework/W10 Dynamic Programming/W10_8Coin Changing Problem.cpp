#include <bits/stdc++.h>

using namespace std;

//? ฟังก์ชันสำหรับหาจำนวนวิธีการแลกเหรียญทั้งหมด
//? รับพารามิเตอร์เป็นจำนวนเงินเป้าหมาย (amount) และรายการมูลค่าเหรียญ (coins)
long long countCoinChangeWays(int amount, const vector<int>& coins) {
    //@ สร้างตาราง dp ขนาด amount + 1 ช่อง เก็บค่าเริ่มต้นเป็น 0
    //* dp[i] = จำนวนวิธีในการรวมเหรียญให้ได้ค่า i
    vector<long long> dp(amount + 1, 0);
    //@ Base case: วิธีทำเงิน 0 บาท คือ 1 วิธี (ไม่ใช้เหรียญเลย)
    dp[0] = 1;
    //@ วนลูปทีละชนิดเหรียญ (ลูปด้านนอกสุดเพื่อป้องกันการนับสลับที่)
    for(int coin : coins) {
        //* อัปเดตตาราง dp สำหรับยอดเงินตั้งแต่ค่าเหรียญนั้นๆ ไปจนถึงเป้าหมาย amount
        for(int i = coin; i <= amount; i++)
            dp[i] = dp[i] + dp[i - coin];
    }
    //* คืนค่าจำนวนวิธีทั้งหมดในการทำยอดเงิน amount
    return dp[amount];
}

int main() {
    int amount, numCoins;
    //* รับค่าจำนวนเงินที่ต้องการแลก (amount) และจำนวนชนิดของเหรียญ (numCoins)
    cin>>amount>>numCoins;
    vector<int> coins(numCoins);
    //* รับมูลค่าของเหรียญแต่ละชนิด
    for(int i = 0; i < numCoins; i++)
        cin>>coins[i];
    //* เรียกใช้ฟังก์ชันและแสดงผลลัพธ์
    long long totalWays = countCoinChangeWays(amount, coins);
    cout<<totalWays<<" ways";
    return 0;
}

