#include <bits/stdc++.h>

using namespace std;

//? ฟังก์ชันหาผลรวมราคาขายที่มากที่สุดจากการตัดท่อนไม้
int maxProfitRodCutting(int rodLength, const vector<int>& prices) {
    //@ สร้างตาราง dp ขนาด rodLength + 1 เก็บค่ากำไรสูงสุดของไม้แต่ละความยาว
    //* เริ่มต้นที่ 0 ทั้งหมด (ครอบคลุม Base Case: dp[0] = 0 ไปในตัว)
    vector<int> dp(rodLength + 1, 0);
    //* คำนวณหาคำตอบตั้งแต่ไม้ความยาว 1 นิ้ว ไล่ขึ้นไปจนถึง rodLength นิ้ว (Bottom-Up)
    for(int i = 1; i <= rodLength; i++) {
        int maxRevenue = 0; //* ตัวแปรเก็บค่าสูงสุดชั่วคราวของความยาว i
        //* ลองพิจารณาการตัดไม้ "ชิ้นแรก" ให้มีความยาว j (ตั้งแต่ 1 ถึง i)
        for(int j = 1; j <= i; j++)
            //* เลือกระหว่างค่าสูงสุดเดิมที่เคยเจอ หรือ ราคาของไม้ความยาว j + กำไรสูงสุดของไม้ที่เหลือ
            maxRevenue = max(maxRevenue, prices[j] + dp[i - j]);
        //* บันทึกค่ากำไรสูงสุดลงในตาราง dp
        dp[i] = maxRevenue;
    }
    //* คืนค่ากำไรสูงสุดของไม้ความยาว rodLength
    return dp[rodLength];
}

int main() {
    int rodLength; //* รับค่าความยาวท่อนไม้ทั้งหมด
    cin>>rodLength;
    //* สร้าง vector ราคา โดยให้มีขนาด rodLength + 1 เพื่อให้ index ตรงกับความยาว 1 ถึง rodLength
    vector<int> prices(rodLength + 1, 0); // ช่อง 0 ไม่ถูกใช้งาน
    for(int i = 1; i <= rodLength; i++) //* รับราคาท่อนไม้ความยาว 1 ถึง rodLength
        cin>>prices[i];
    cout<<maxProfitRodCutting(rodLength, prices)<<"\n";
    return 0;
}

