#include <bits/stdc++.h>
using namespace std;

//? ฟังก์ชันหาจำนวนสินค้ามากที่สุดที่ซื้อได้ด้วยเงินพอดี budget บาท
int maxItemsForExactAmount(int numItem, int budget, const vector<int>& prices) {
    //@ สร้างตาราง dp ขนาด budget + 1 ช่อง
    //* กำหนดค่าเริ่มต้นเป็น -1 ทั้งหมด เพื่อใช้เช็คว่ายอดเงินนั้นๆ เป็นไปได้หรือไม่
    vector<int> dp(budget + 1, 0);
    //@ วนลูปพิจารณาสินค้าทีละชิ้น
    for(int i = 0; i < numItem; i++) {
        int price = prices[i];
        //* วนลูปยอดเงินจาก budget ลงมาหา price (เดินถอยหลังป้องกันการใช้ของชิ้นเดิมซ้ำ)
        for(int j = budget; j >= price; j--) {
            //* อัปเดตค่า: เลือกระหว่าง จำนวนชิ้นเดิมที่ยอดเงิน j หรือ เอาจำนวนชิ้นที่ยอดเงิน (j - price) บวกของใหม่ 1 ชิ้น
            dp[j] = max(dp[j], dp[j - price] + 1);
        }
    }
    //* คืนค่าจำนวนชิ้นที่มากที่สุดสำหรับยอดเงิน budget พอดี
    return dp[budget];
}

int main() {
    int numItem, budget;
    //* รับค่า numItem (จำนวนสินค้า) และ budget (เงินเริ่มต้น)
    cin>>numItem>>budget;
    vector<int> prices(numItem);
    //* รับราคาสินค้าแต่ละชิ้น
    for(int i = 0; i < numItem; i++)
        cin>>prices[i];
    //* เรียกใช้งานฟังก์ชันและพิมพ์ผลลัพธ์
    cout<<maxItemsForExactAmount(numItem, budget, prices)<<endl;
    return 0;
}

