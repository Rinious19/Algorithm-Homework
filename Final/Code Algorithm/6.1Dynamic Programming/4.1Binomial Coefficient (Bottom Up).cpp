#include <bits/stdc++.h>
using namespace std;

//? ฟังก์ชันหาค่า Binomial Coefficient แบบ Bottom-Up
unsigned long long C_Bottom_Up(int n, int k){
    //@ สร้างตาราง 2 มิติขนาด (n + 1) x (k + 1) 
    vector<vector<unsigned long long>> memory(n + 1, vector<unsigned long long>(k + 1));
    //@ Base Cases: ขอบซ้ายสุด (col == 0) หรือ ขอบขวาสุด (row == col) มีค่าเป็น 1 เสมอ
    for(int i = 0; i <= n; i++){
        memory[i][0] = 1;
        if(i <= k) memory[i][i] = 1;
    }
    //@ ค่อยๆ เติมตารางทีละแถว (เหมือนวาดสามเหลี่ยมปาสกาลทีละชั้น)
    for(int i = 1; i <= n; i++){
        //* ลูป j จะวิ่งถึงแค่ i หรือ k (เอาตัวที่น้อยกว่า) 
        // เพราะเราไม่สามารถเลือกของ 5 ชิ้น จากของ 3 ชิ้นได้ (j <= i)
        for(int j = 1; j < i && j <= k; j++)
            //* เอาค่าจากแถวบน 2 ตัวมาบวกกัน
            memory[i][j] = memory[i - 1][j - 1] + memory[i - 1][j];
    }
    return memory[n][k];
}

int main(){
    int n, k;
    cin>>n>>k;
    if(k < 0 || k > n){
        cout<<"ค่า k ต้องอยู่ระหว่าง 0 ถึง n"<<endl;
        return 0;
    }
    unsigned long long result = C_Bottom_Up(n, k);
    cout<<"C("<<n<<", "<<k<<") = "<<result<<endl;
    return 0;
}

