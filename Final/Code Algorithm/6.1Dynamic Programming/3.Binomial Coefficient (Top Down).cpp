#include <bits/stdc++.h>
using namespace std;

//? ฟังก์ชันหาค่า Binomial Coefficient แบบ Top-Down
long long C_Top_Down(int n, int k, vector<vector<long long>>& memory){
    //@ 1. Base Cases: เลือก 0 ชิ้น หรือ เลือกทั้งหมด n ชิ้น จะมีแค่ 1 วิธี
    if(k == n || k == 0)
        return 1;
    //@ 2. Check Memo: ถ้าเคยคำนวณแล้ว ให้หยิบคำตอบมาใช้ได้เลย
    if(memory[n][k] != -1)
        return memory[n][k];
    //@ 3. Recursive Step + Store Result:
    // อ้างอิงจากสูตร C(n, k) = C(n-1, k-1) + C(n-1, k)
    memory[n][k] = C_Top_Down(n - 1, k - 1, memory) + C_Top_Down(n - 1, k, memory);
    return memory[n][k];
}

int main(){
    int n, k;
    cin>>n>>k;
    vector<vector<long long>> memory(n + 1, vector<long long>(k + 1, -1));
    if(k < 0 || k > n){
        cout<<"ค่า k ต้องอยู่ระหว่าง 0 ถึง n"<<endl;
        return 0;
    }
    long long result = C_Top_Down(n, k, memory);
    cout<<"C("<<n<<", "<<k<<") = "<<result<<endl;
    return 0;
}

