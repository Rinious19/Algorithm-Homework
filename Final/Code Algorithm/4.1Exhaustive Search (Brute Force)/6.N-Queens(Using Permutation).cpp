#include <bits/stdc++.h>
using namespace std;

int N = 4;

int fnCallCount = 0; //* นับจำนวนครั้งที่ฟังก์ชันถูกเรียก (Nodes in tree)
int leafCount = 0; //* นับจำนวนรูปแบบที่สลับเสร็จแล้ว (Permutations ทั้งหมด = N!)
int ansCount = 0; //* นับจำนวนคำตอบที่ถูกต้อง (Valid Solutions)

//? ฟังก์ชันหา N-Queens ด้วยวิธี Permutation
//* col: อาเรย์เก็บตำแหน่งคอลัมน์ (เช่น [1, 3, 0, 2])
//* index: ตำแหน่งที่เรากำลังจะตัดสินใจสลับเปลี่ยน (Pivot)
void nQueens(vector<int>& col, int index){
    fnCallCount++; //* นับว่าเข้าฟังก์ชันมาแล้ว
    // @ Base Case: เมื่อ index เลื่อนไปจนสุดขอบ (สลับครบทุกตัวแล้ว)
    if(index == N){
        leafCount++; 
        bool isValid = true; 
        //* Forward Checking: ตรวจสอบว่าควีนชนกันมั้ย วนลูปเช็คจับคู่ทุกตัว 
        for(int i=0; i<N; i++){
            for(int j=i+1; j<N; j++){
                //! เช็คเฉพาะแนวทแยง (Diagonal) ไม่ต้องเช็คแนวตั้งหรือแนวนอน (ดูคำอธิบายด้านล่าง)
                if(abs(i-j) == abs(col[i] - col[j])){
                    isValid = false; //* ถ้ากินกันในแนวทแยง ถือว่าผิด
                    break;
                }
            }
        }
        //* ถ้าไม่ผิดกฎเลย ให้พิมพ์คำตอบ
        if(isValid){
            ansCount++;
            for(int i=0; i<N; i++)
                cout<<col[i]<<" ";
            cout<<endl;
        }
    }
    // @ Recursive Step: การสลับตำแหน่ง (Permutation Logic)
    else{
        for(int i=index; i<N; i++){ //! i เริ่มที่ index เพราะป้องกันการเรียงซ้ำแบบเดิม
            //* 1. Swap: สลับตัวที่ index กับตัวที่ i
            // เพื่อดึงเอาเลขตัวอื่นมาลองวางในตำแหน่ง index นี้
            swap(col[index], col[i]); 
            //* 2. Recurse: ขยับไปทำตำแหน่งถัดไป
            nQueens(col, index + 1);
            //* 3. Backtrack: สลับคืนที่เดิม
            // เพื่อให้รอบถัดไปของลูป (i++) array กลับมาเป็นสภาพเดิมก่อนสลับ
            swap(col[index], col[i]);            
        }
    }
}

int main(){
    // สร้าง array เริ่มต้น [0, 1, 2, 3]
    // หมายถึง แถว 0 อยู่คอลัมน์ 0, แถว 1 อยู่คอลัมน์ 1 ...
    vector<int> col(N);
    for(int i=0; i<N; i++) col[i] = i;
    nQueens(col, 0); //* เริ่มสลับจาก index 0
    cout<<"Function Call Count = "<<fnCallCount<<", Leaf Count = "<<leafCount<<", Ans Count = "<<ansCount<<endl;
    return 0;
}

