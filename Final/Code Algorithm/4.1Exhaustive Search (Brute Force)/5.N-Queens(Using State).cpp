#include <bits/stdc++.h>
using namespace std;

int N = 4; //* กำหนดขนาดบอร์ด 4x4
int col[4]; //* อาเรย์เก็บตำแหน่ง: col[row] = column (แถวที่ row วาง Queen ที่คอลัมน์ไหน)

int fnCallCount = 0;   //* นับโหนดทั้งหมดใน Tree
int leafCount = 0;     //* นับจำนวนบอร์ดที่วางครบ 4 ตัว (Base Case)
int ansCount = 0; //* นับจำนวนคำตอบที่ถูกต้อง

//? ฟังก์ชัน Recursive หาตำแหน่ง Queen
// index: หมายถึง "แถว" ปัจจุบันที่เรากำลังจะวาง Queen
// count: ตัวนับจำนวนรูปแบบทั้งหมดที่ถูกสร้างขึ้นมา (ทั้งถูกและผิดกฎ)
void nQueens(int index, int& count){
    fnCallCount++;
    //@ Base Case: เมื่อวาง Queen ครบทุกแถวแล้ว (index วิ่งไปจนถึง N)
    if(index == N){
        leafCount++; //* นับจำนวนครั้งที่สร้างบอร์ดเต็มได้ (จะนับได้ 4^4 = 256 ครั้ง) หรือ Leaf Node
        bool isValid = true; //* สมมติให้บอร์ดนี้ถูกต้องไว้ก่อน (Flag)
        //* Forward Checking: วนลูปจับคู่ Queen ทุกตัวบนบอร์ดเพื่อดูว่ามีคู่ไหนกินกันไหม
        for(int i = 0; i < N; i++){
            for(int j = i + 1; j < N; j++){
                //* เช็ค 2 เงื่อนไข:
                // 1. abs(j-i) == abs(col[j]-col[i]) : เช็คแนวทแยง (Diagonal Check)
                // 2. col[j] == col[i] : เช็คแนวตั้ง (Column Check) *จำเป็นต้องเช็คเพราะโค้ดนี้อนุญาตให้วางซ้ำคอลัมน์ได้ตอนสร้าง
                if(abs(j - i) == abs(col[j] - col[i]) || col[j] == col[i]){
                    isValid = false; //* ถ้าเจอคู่ที่กินกัน ให้ปรับค่าเป็นไม่ผ่าน
                    break; //* เจอคู่ที่ชนกันแล้ว เลิกเช็คต่อเพื่อความเร็ว
                }
            }
        }
        //* ถ้าผ่านการตรวจสอบให้พิมพ์ผลลัพธ์
        if(isValid){
            ansCount++;
            cout<<"Solution: ";
            for(int i = 0; i < N; i++)
                cout<<col[i]<<" ";
            cout<<endl;
        }
    }
    //@ Recursive Step: ถ้ายังวางไม่ครบทุกแถว
    else{
        //* ลองวาง Queen ในแถวที่ 'index' โดยลองทุกคอลัมน์ตั้งแต่ 0 ถึง N-1
        for(int i = 0; i < N; i++){
            col[index] = i; //* ลองวาง Queen ในแถว index ที่คอลัมน์ i
            nQueens(index + 1, count); //* เรียกตัวเองไปทำแถวถัดไป (index + 1)
            //! โค้ดนี้ไม่มีการเช็ค isValid ตรงนี้ ทำให้มันสร้าง state ที่ผิดกฎลงไปด้วย (Brute Force เต็มรูปแบบ)
        }
    }
}

int main(){
    int count = 0;
    nQueens(0, count); //* เริ่มวางจากแถวที่ 0
    cout<<"Function Call Count = "<<fnCallCount<<", Leaf Count = "<<leafCount<<", Ans Count = "<<ansCount<<endl;
    return 0;
}

