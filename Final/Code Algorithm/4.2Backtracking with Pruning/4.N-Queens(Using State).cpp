#include <bits/stdc++.h>
using namespace std;

int N = 4;
int col[4]; //* เก็บตำแหน่งคอลัมน์ของ Queen: col[แถว] = คอลัมน์

int fnCallCount = 0; //* นับจำนวนครั้งที่เรียกฟังก์ชัน (จำนวน Node ใน Tree ที่เข้าไปเยี่ยมชม)
int tryCount = 0; //* นับจำนวนครั้งที่ "พยายามวาง" (ใน Loop i)
int ansCount = 0; //* นับจำนวนคำตอบที่ถูกต้อง (Leaf Node ที่ผ่านเงื่อนไข)

//? ฟังก์ชัน Backtracking
//* index: หมายถึง "แถว" (Row) ปัจจุบันที่เรากำลังจะวาง Queen
void nQueens(int index){
    fnCallCount++; //* นับว่าเข้ามาในฟังก์ชันแล้ว 1 ครั้ง
    // @ Base Case: ถ้าวางครบ N ตัวแล้ว (index วิ่งไปจนถึง N) ถูกกรองคำตอบมาเรียบร้อย
    if(index == N){
        ansCount++; //* เจอคำตอบที่ถูกต้องเพิ่ม 1 แบบ
        //* พิมพ์กระดานคำตอบ
        for(int i=0; i<index; i++)
            cout<<col[i]<<" ";
        cout<<endl;
    }
    // @ Recursive Step: ยังวางไม่ครบ ต้องหาที่วางในแถว 'index'
    else{
        //* ลองวาง Queen ในแถวที่ 'index' โดยไล่ลองทุกคอลัมน์ (0 ถึง N-1)
        for(int i=0; i<N; i++){
            tryCount++; //* นับความพยายามในการวาง
            col[index] = i; //* สมมติว่าวาง Queen ที่แถว index, คอลัมน์ i
            //* ส่วนของการตรวจสอบ (Validation / Pruning)
            bool isValid = true;
            //! ทำไมถึงใช้ Loop เดียว? (ดูคำอธิบายด้านล่าง)
            //* เช็คเทียบกับ Queen "รุ่นพี่" ที่วางไปก่อนหน้านี้ (ตั้งแต่แถว 0 ถึง index-1)
            for(int j=0; j<index; j++){
                //* เช็ค 2 เงื่อนไข:
                // 1. แนวทแยง: abs(แถวใหม่ - แถวเก่า) == abs(คอลัมน์ใหม่ - คอลัมน์เก่า)
                // 2. แนวตั้ง: คอลัมน์ตรงกันไหม (col[j] == col[index])
                if(abs(j - index) == abs(col[j] - col[index]) || col[j] == col[index]){
                    isValid = false; // ถ้าชนกันแม้แต่ตัวเดียว ก็ถือว่าวางไม่ได้
                    break; //* หยุดเช็คทันทีเพื่อความเร็ว (Optimization)
                }
            }
            //* ถ้าวางแล้วปลอดภัย (isValid == true) ถึงจะอนุญาตให้ไปต่อ
            if(isValid)
                nQueens(index + 1); //* ไปวางแถวถัดไป (Backtracking Step)
            //* ถ้าไม่ผ่าน (isValid == false) ก็จะจบรอบ Loop นี้ แล้วไปลอง col ถัดไป (i++)
            //* นี่คือการ "ตัดกิ่ง" (Pruning)
        }
    }
}

int main(){
    nQueens(0);
    cout<<"Function Call Count = "<<fnCallCount<<", Try Count = "<<tryCount<<", Ans Count = "<<ansCount<<endl;
    return 0;
}

