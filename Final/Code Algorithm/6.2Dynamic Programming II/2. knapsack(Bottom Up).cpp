#include <bits/stdc++.h>
using namespace std;

vector<int> value = {1, 4, 5, 7};
vector<int> weight = {1, 3, 4, 5};
int Knapsack_Bottom_Up(int n, int maxWeight){
    //@ 1. สร้างตาราง DP Table (Tabulation)
    vector<vector<int>> memo(n + 1, vector<int>(maxWeight + 1, -1));
    //@ 2. Base Case: น้ำหนักกระเป๋าเป็น 0 หรือไม่มีของให้เลือก มูลค่าต้องเป็น 0
    for(int i=0;i<=n;i++)
        memo[i][0] = 0;
    for(int j=0;j<=maxWeight;j++)
        memo[0][j] = 0;
    //@ 3. เริ่มเติมตารางจากล่างขึ้นบน
    for(int i=1;i<=n;i++){
        for(int j=1;j<=maxWeight;j++){
            if(weight[i - 1] <= j)
                memo[i][j] = max(value[i - 1] + memo[i - 1][j - weight[i - 1]], memo[i - 1][j]);
            else
                memo[i][j] = memo[i - 1][j];
        }
    }
    return memo[n][maxWeight];
}

int main(){
    int n = value.size();
    int maxWeight = 7;
    int result = Knapsack_Bottom_Up(n, maxWeight);
    cout<<result<<endl;
    return 0;
}

