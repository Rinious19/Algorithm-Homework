#include <bits/stdc++.h>
using namespace std;

//? คลาส Edge: โครงสร้างเก็บข้อมูลเส้นเชื่อม
class Edge{
public:
    int u, v, weight; //* โหนดต้นทาง (u), โหนดปลายทาง (v), และน้ำหนักของเส้นเชื่อม (weight)
    //@ method สำหรับเปรียบเทียบน้ำหนัก ใช้เป็นเงื่อนไขให้ฟังก์ชัน sort() 
    static bool compareEdge(Edge a, Edge b) { 
        return a.weight < b.weight;  //* เรียงลำดับเส้นเชื่อมจาก "น้ำหนักน้อยไปมาก"
    }
};

//? คลาส DisjointSet (Union-Find): ตัวป้องกันการเกิดวงวน (Cycle)
class DisjointSet{
public:
    vector<int> parent; //* อาเรย์เก็บว่าใครคือ "หัวหน้ากลุ่ม" ของโหนดนั้นๆ
    //@ Constructor: กำหนดค่าเริ่มต้นให้ทุกโหนดเป็นหัวหน้ากลุ่มของตัวเอง (ยังไม่เชื่อมกับใคร)
    DisjointSet(int n){ //* n คือจำนวนโหนด
        parent.resize(n + 1); //* resize = กำหนดขนาดของอาเรย์ให้มีพื้นที่เก็บข้อมูลได้ n+1 ตัว (index 0 ถึง n)
        for(int i = 0; i <= n; i++)
            parent[i] = i;
    }
    //@ ฟังก์ชัน find: ใช้หา "หัวหน้ากลุ่มสูงสุด" ของโหนด i 
    //* ทริค: มีการทำ Path Compression (parent[i] = find(...)) เพื่อหดเส้นทางให้สั้นลง ค้นหารอบหน้าจะเร็วขึ้น
    int find(int i) { 
        return (parent[i] == i) ? i : (parent[i] = find(parent[i])); 
    }
    //@ ฟังก์ชัน unite: ตรวจสอบและจับรวมกลุ่ม
    bool unite(int i, int j){
        int root_i = find(i); // หาหัวหน้าของ i
        int root_j = find(j); // หาหัวหน้าของ j
        //* ถ้าหัวหน้าคนละคนกัน แปลว่า "อยู่คนละกลุ่ม" (ลากเส้นเชื่อมแล้วไม่เกิดวงวน)
        if (root_i != root_j){
            parent[root_i] = root_j; //* ให้กลุ่ม i ไปขึ้นตรงกับกลุ่ม j (จับยุบรวมกัน)
            return true; // คืนค่า true ยืนยันว่าเชื่อมต่อสำเร็จ
        }
        //! ถ้าหัวหน้าคนเดียวกัน แปลว่า "ลากเส้นแล้วจะเกิดวงวน" (Cycle)
        return false; // คืนค่า false ปฏิเสธการเชื่อม
    }
};

//? ฟังก์ชันหลักของ Kruskal's Algorithm
void solveKruskal(const vector<Edge>& edges, int numNodes){
    DisjointSet dsu(numNodes); //* สร้างระบบจัดการกลุ่มตามจำนวนโหนด
    vector<Edge> mstResult; //* เวกเตอร์สำหรับเก็บเส้นเชื่อมที่ได้รับเลือกเข้าสู่ MST
    long long totalWeight = 0; //* ตัวแปรเก็บผลรวมน้ำหนักที่น้อยที่สุด
    //@ วนลูปพิจารณาเส้นเชื่อมทีละเส้น (ซึ่งถูกเรียงจากน้อยไปมากมาแล้ว)
    for(auto &edge : edges){
        //* ลองเชื่อมโหนด u และ v เข้าด้วยกัน
        //* ถ้า unite คืนค่า true (ไม่เกิดวงวน) ถึงจะเอาเส้นนี้เข้า MST
        if (dsu.unite(edge.u, edge.v)){
            mstResult.push_back(edge); // จดเส้นเชื่อมนี้ลงในผลลัพธ์
            totalWeight += edge.weight; // บวกน้ำหนักสะสม
        }
        //! กฎของต้นไม้แผ่ทั่ว (Spanning Tree): จำนวนเส้นเชื่อมจะเท่ากับ (จำนวนโหนด - 1) เสมอ
        //* ถ้าเลือกครบแล้ว ให้หยุดลูปทันทีเพื่อประหยัดเวลา (Early Exit)
        if (mstResult.size() == numNodes - 1)
            break; //* ครบเงื่อนไข MST
    }
    cout<<"Total MST Weight: "<<totalWeight<<endl;
    for(auto &e : mstResult)
        cout<<e.u<<" - "<<e.v<<" : "<<e.weight<<endl;
}

int main(){
    int numNodes, numEdges;
    cin>>numNodes>>numEdges;
    vector<Edge> edges(numEdges);
    //* รับค่าข้อมูลเส้นเชื่อมทั้งหมด
    for(int i = 0; i < numEdges; i++)
        cin>>edges[i].u>>edges[i].v>>edges[i].weight;
    //* เรียงลำดับเส้นเชื่อมจากน้อยไปมาก โดยใช้เงื่อนไขจาก method compareEdge ที่เราสร้างไว้ในคลาส Edge
    sort(edges.begin(), edges.end(), Edge::compareEdge);
    //! Class::Method = เรียกใช้ method ที่เป็น static ของคลาส Edge โดยไม่ต้องสร้างอ็อบเจ็กต์ของคลาสนั้น
    solveKruskal(edges, numNodes); // โยนเส้นเชื่อมที่เรียงแล้ว เข้าไปหา MST
    return 0;
}

