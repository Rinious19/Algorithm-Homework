#include <bits/stdc++.h>
using namespace std;

//? ฟังก์ชันหาโหนดที่ยังไม่เคยไป และมีระยะทางสะสมสั้นที่สุด
int findMinDistanceNode(const vector<int>& distances, const vector<bool>& visited){
    int minVal = INT_MAX, minIndex = -1;
    for(int i = 0; i < distances.size(); i++){
        //* ถ้ายังไม่เคยแวะ และระยะทางน้อยกว่าหรือเท่ากับค่าน้อยสุดปัจจุบัน
        if(!visited[i] && distances[i] <= minVal){
            minVal = distances[i];
            minIndex = i;
        }
    }
    return minIndex;
}

//? ฟังก์ชันหลัก อัลกอริทึม Dijkstra
void dijkstra(const vector<vector<int>>& graph, int startNode){
    //* distances: เก็บระยะทางสั้นสุดจากจุดเริ่มต้น ไปยังโหนดอื่นๆ
    //* visited: เก็บสถานะว่าเราเคยหาเส้นทางที่สั้นที่สุดของโหนดนี้เสร็จหรือยัง true = เคยไปแล้ว, false = ยังไม่เคยไป
    //* previousNode: เก็บว่าก่อนจะมาถึงโหนดนี้ เราเดินทางมาจากโหนดไหน (ใช้สำหรับย้อนรอยเส้นทาง)
	int numVertices = graph.size();
    vector<int> distances(numVertices, INT_MAX); // เริ่มต้นเป็น INT_MAX หมายถึงยังไม่รู้ระยะทาง
    vector<int> previousNode(numVertices, -1); // เริ่มต้นเป็น -1 หมายถึงยังไม่มีโหนดก่อนหน้า
    vector<bool> visited(numVertices, false); // เริ่มต้นเป็น false หมายถึงยังไม่เคยไปโหนดไหนเลย
    distances[startNode] = 0; //* จุดเริ่มต้นมีระยะทางเป็น 0 เสมอ
    //@ วนลูปเพื่อยืนยันระยะทางที่สั้นที่สุดให้ครบทุกโหนด ไม่รวมโหนดสุด (numVertices - 1) 
    //  เพราะโหนดสุดท้ายจะถูกยืนยันโดยโหนดก่อนหน้าแล้ว
    for(int i = 0; i < numVertices - 1; i++){
        //* 1. เลือกโหนด 'u' ที่ยังไม่เคยไป และมีระยะทางสั้นที่สุดในขณะนี้
        int u = findMinDistanceNode(distances, visited);
        //* กราฟอาจจะขาดจากกัน ถ้าหาโหนดที่เชื่อมต่อไม่ได้แล้วให้หยุดทำงาน
        if(u == -1 || distances[u] == INT_MAX) break;
        visited[u] = true; // มาร์คว่าเราประมวลผลโหนดนี้เสร็จแล้ว
        //* 2. อัปเดตระยะทางของโหนดเพื่อนบ้าน 'v' ของโหนด 'u'
        for(int v = 0; v < numVertices; v++){
            //* เงื่อนไขในการอัปเดต:
            // - ยังไม่เคยประมวลผล (visited[v] == false)
            // - มีเส้นเชื่อมระหว่าง u และ v (graph[u][v] != 0)
            // - ไม่ใช่โหนดที่ขาดการเชื่อมต่อ (distances[u] != INT_MAX เพื่อกัน Overflow)
            // - ระยะทางใหม่ (ผ่าน u) สั้นกว่าระยะทางเดิมที่เคยบันทึกไว้
            if(!visited[v] && graph[u][v] != 0 && distances[u] != INT_MAX && 
               (distances[u] + graph[u][v] < distances[v])){
                distances[v] = distances[u] + graph[u][v];
                previousNode[v] = u; // จดจำไว้ว่าเราเดินทางมาหา v ผ่านทาง u
            }
        }
    }
    //@ แสดงผลลัพธ์: ระยะทางสั้นที่สุดจากจุดเริ่มต้นไปยังโหนดอื่นๆ พร้อมเส้นทาง
    for(int i = 0; i < numVertices; i++){
        if(i != startNode && distances[i] != INT_MAX){
            cout<<"Path from "<<startNode<<" to "<<i<<" : ";
            vector<int> path;
            //* วนลูปย้อนรอยจากปลายทาง กลับไปหาจุดเริ่มต้นผ่าน array previousNode
            for(int curr = i; curr != -1 ; curr = previousNode[curr])
                path.push_back(curr);
            //* พิมพ์จากหลังมาหน้า (เพราะตอนเก็บ เราเก็บถอยหลังจากปลายทาง)
            for(int j = path.size() - 1; j > 0; j--)
                cout<<path[j]<<" -> ";
            //* พิมพ์โหนดปลายทาง และระยะทางรวมที่สั้นที่สุด
            cout<<path[0]<<" | Total distance = "<<distances[i]<<endl;
        }
    }
}

int main(){
    int numVertices, numEdges;
    cin>>numVertices>>numEdges;
    //* สร้างตาราง Adjacency Matrix ขนาด n x n และกำหนดค่าเริ่มต้นเป็น 0 ทุกช่อง
    vector<vector<int>> graph(numVertices, vector<int>(numVertices, 0));
    for(int i = 0; i < numEdges; i++){
        int start, dest, weight;
        cin>>start>>dest>>weight;
        graph[start][dest] = weight;
        graph[dest][start] = weight;
    }
    //* เรียกใช้ฟังก์ชัน โดยกำหนดโหนดเริ่มต้นเป็น 0
    dijkstra(graph, 0);
    return 0;
}

